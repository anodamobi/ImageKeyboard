import UIKit

extension UIColor {
    
    @nonobjc class var gdkBlue: UIColor {
        return UIColor(red: 15.0 / 255.0, green: 133.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var gdkLightBlue: UIColor {
        return UIColor(red: 79.0 / 255.0, green: 165.0 / 255.0, blue: 255.0 / 255.0, alpha: 1.0)
    }
}
